public class Ex_229 {
	public static void main(String[] args) {
		char c;

		c = 'A';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);

		c = 'B';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
		
		c = 'C';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
		
		c = 'a';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);

		c = 'b';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	
		c = 'c';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	
		c = '0';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	
		c = '1';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	
		c = '2';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);

		c = '$';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	
		c = '*';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	
		c = '+';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);

		c = '/';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);

		c = ' ';
		System.out.printf("The character %c has the value %d%n",
					c, (int)c);
	}
}